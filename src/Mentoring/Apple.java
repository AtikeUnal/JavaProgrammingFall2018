package Mentoring;

public class Apple {
 double weight;
 String color;
 String taste;
 String size;
 
 public Apple(int weight, String color, String taste, String size) {
	 this.weight=weight;
	 this.color=color;
	 this.taste=taste;
	 this.size=size;
	 
	 
	 
	
 }
 public void printInfo() {
	 System.out.println("Weight: "+weight);
	 System.out.println("Color: "+color);
	 System.out.println("Taste: "+taste);
	 System.out.println("Size: "+size);
	 
 }
	
}
